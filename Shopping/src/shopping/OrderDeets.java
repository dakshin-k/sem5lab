        for(int i=0;i<8&&!items[i].getText().isEmpty();i++)
        {
            DataBase.delete("update inventory"
                    + " set qty = qty - "+qtys[i].getText()+
                    " where pid = "+items[i].getText());
        }
        JOptionPane.showMessageDialog(null, "Done.");
        new StaffHome(login).setVisible(true);
        dispose();
